SELECT 
    V.VEN_NOME AS 'NOME DO VENDEDOR',
    S.SEX_SEXO AS 'SEXO DO VENDEDOR'
FROM TB_VENDEDORES V JOIN TB_SEXO S ON V.VEN_SEX_CODIGO = S.SEX_CODIGO
GO

SELECT 
    P.PRO_DES_PRODUTO AS 'DESCRIÇÃO DO PRODUTO',
    M.MAR_MARCA AS 'MARCA DO PRODUTO',
    D.DEP_NOME AS 'DEPARTAMENTO'
FROM TB_PRODUTOS P
JOIN TB_MARCAS M ON P.PRO_MAR_CODIGO = M.MAR_CODIGO
JOIN TB_DEPARTAMENTOS D ON P.PRO_DEP_CODIGO = D.DEP_CODIGO
GO